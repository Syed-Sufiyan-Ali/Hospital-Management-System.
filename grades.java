public class grades {
    public static void main (String[] args){
        int marking = 41;
        if (marking >= 90 ){
            System.out.println("A+");
        }
        else if (marking >= 80){
            System.out.println("A");
        }
        else if (marking >= 70){
            System.out.println("B");
        }
        else if (marking >= 60){
            System.out.println("C");
        }
        else if (marking >= 50){
            System.out.println("D");
        }
        else if (marking >= 40){
            System.out.println("E");
        }
        else {
            System.out.println("F");
        }
    }
    
}
